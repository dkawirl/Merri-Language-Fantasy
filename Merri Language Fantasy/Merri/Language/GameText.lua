function GameText:GetPronouns()
	-- Return list of pronouns (target language) and descriptions (helper language)
	
	local pronouns = {}
	for key, pronoun in pairs( GameText.Target.pronouns ) do
		p = pronoun
		p.description = GameText:Get( "helper", "pronoun description " .. pronoun.type )
		table.insert( pronouns, p )
	end

	return pronouns
end

function GameText:Setup( options )
    GameText.HelperText = {}
    GameText.TargetText = {}
	-- These two tables will be for the actual languages, loaded into the game via setup function call.
	GameText.Helper = {}
	GameText.Target = {}
    GameText:SetupLanguages()
end

-- Common language text (from GetText.lua)

function GameText:GetFromLanguageCommon( cat, lang, id )
    local language = lang
    local category = ""
    if ( cat == "helper" ) then
        category = "HelperText"

    else
        category = "TargetText"

    end

    if ( GameText[ category ] == nil ) then
        return "Invalid category " .. category

    elseif ( GameText[ category ][ language ] == nil ) then
        return "Invalid language " .. language

    elseif ( GameText[ category ][ language ][ id ] == nil ) then
        return "Invalid id " .. id

    else
        return GameText[ category ][ language ][ id ]

    end
end

function GameText:GetCommon( cat, id )
    local language = ""
    local category = ""
    if ( cat == "helper" ) then
        language = GLOBAL_CONFIG.HELPER_LANGUAGE
        category = "HelperText"

    else
        language = GLOBAL_CONFIG.TARGET_LANGUAGE
        category = "TargetText"

    end

    if ( GameText[ category ] == nil ) then
        return "Invalid category " .. category

    elseif ( GameText[ category ][ language ] == nil ) then
        return "Invalid language " .. language

    elseif ( GameText[ category ][ language ][ id ] == nil ) then
        return "Invalid id " .. id

    else
        return GameText[ category ][ language ][ id ]

    end
end

